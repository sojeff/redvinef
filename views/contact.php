		<h3 class="title">About</h3>

		<p><strong class="highlight">SO:KNO</strong> or <em class="fade">(<strong class="highlight">SO</strong>cial <strong class="highlight">KNO</strong>wledge)</em> is a platform that enables and promotes the creation, consumption and sharing of knowledge, creating a new standard of social learning in a simple to use application.</p>
		<br><br>

		<h3 class="title">How it Works</h3>

		<p><strong>SO:KNO</strong> turns any topic into an online community and makes connections between content and with people who have similar interests.</p>

		<p><strong>SO:KNO</strong> harvests and collects each users efforts to gain knowledge in a simple, fun and engaging experience that allows users to find and save the best information for themselves, while simultaneously creating a pool of collective knowledge that promotes the sharing of knowledge and expertise.</p>

		<p>These pools of knowledge, or <strong class="highlight">Knowledge Cells</strong>, as well call them, are the heart and soul of <strong>SO:KNO</strong>.  Each Knowledge Cell is composed of Content, Community and Collaboration living together harmoniously.  Knowledge Cells promote adaptive discovery, enabling users to safely find content, communities and people worldwide who share a common interest. When a user engages, they connect to relevant Knowledge Cells where the best people and content are always dynamically in motion.</p>

		<p>The best people are ranked by the contributions to the community, the best content is ranked by the people who interact with the content based upon their credibility. As users continue using <strong>SO:KNO</strong>, they are cultivating the information, so the content continues to improve and become even more relevant, leading to greater knowledge and understanding on any topic.</p>
		
		<p><strong>SO:KNO</strong> also provides users with a unique <strong class="highlight">PersonaGraf</strong>, a marketable, living and visual representation of a user that creates a unique awareness that describes who you are NOT what you are. Each user builds a Persona that is uniquely theirs, that provides a living and dynamic visual picture of who they are as an individual, their strengths and abilities and where they might find the most enjoyment in their work or their personal lives.  Users can gain credibility and ranking within each area of expertise by sharing their knowledge and content with others and may leverage their Persona by marketing themselves within the <strong>SO:KNO</strong> communities.</p>

		<p>This allows users to monetize their talents both inside or outside the <strong>SO:KNO</strong> platform.  Within the workforce, organizations can use the PersonaGraf to transform their teams by putting the right people in the right positions. Within Education, the PersonaGraf could paint a more granular and meaningful picture of what a student actually knows, or is interested in, than a standardized-test score or a letter grade.  The PersonaGraf could follow the user throughout their lifetimes, being displayed on websites or blogs and included in college applications and resumes.</p>

		<p>All of this leads the individual to a journey of understanding and awareness about their true talents that can be better leveraged for themselves and their organizations.  Imagine the possibilities if we realign people to their natural talents, then created an opportunity for them to be recognized and connected to the industries and leaders in their area of expertise?  This will transform the way that people, businesses, and universities engage to find and share knowledge and change the way the world learns and works.</p>
